package Java_04_클래스와객체_2035_Lv2;

public class User {

	String id;
	String password;
	String name;
	String email;
	int age;
	
	public User() {
	}
	
	public User(String id, String password, String name, String email, int age) {
		this.id = id;
		this.password = password;
		this.name = name;
		this.email = email;
		this.age = age;
	}
}
